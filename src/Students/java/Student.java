package Students.java;
    public class Student {

        public int stuNO;//编号
        public String stuName;//姓名
        public char stuSex;//性别
        public int stuAge;//年龄
        public String stuTel;//电话

        public Student(int no, String name, char sex, int age, String tel) {
            stuNO = no;
            stuName = name;
            stuSex = sex;
            stuAge = age;
            stuTel = tel;
        }

    }
